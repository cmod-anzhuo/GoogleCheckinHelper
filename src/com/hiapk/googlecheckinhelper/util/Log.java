package com.hiapk.googlecheckinhelper.util;

public class Log {

	public static final boolean DEBUG = false;

        public static void v(String LOGTAG, String msg) {
                if (DEBUG) {
                        android.util.Log.v(LOGTAG, msg);
                }
        }

        public static void e(String LOGTAG, String msg) {
                android.util.Log.e(LOGTAG, msg);
        }
        
        public static void e(String LOGTAG, String msg, Throwable e){
        	android.util.Log.e(LOGTAG, msg, e);
        }
}
